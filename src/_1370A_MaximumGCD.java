import java.util.Scanner;

public class _1370A_MaximumGCD {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int n = input.nextInt();
        for (int i = 0; i < n; i++) {
            int x = input.nextInt();
            System.out.println(x/2);
        }
    }
}
